	void Generate(int[]Room ,int StartPositionX, int StartPositionY)
	{
		for (int i = 0; i < 30; i++) {
			for (int j = 0; j < 40; j++) {
				switch (Room[i * 40 + j])
				{
				case 1: GameObject CornerTopLeft = (GameObject)GameObject.Instantiate(CoinHautGauche, new Vector3(j + StartPositionX + 0.5f,i*-1 -0.5f + StartPositionY,0), this.transform.rotation);
				break;

				case 2: GameObject CornerTopRight = (GameObject)GameObject.Instantiate(CoinHautDroit, new Vector3(j + StartPositionX + 0.5f,i*-1 -0.5f + StartPositionY,0), this.transform.rotation);
					break;

				case 3: GameObject CornerBottomLeft = (GameObject)GameObject.Instantiate(CoinBasGauche, new Vector3(j + StartPositionX + 0.5f,i*-1 -0.5f + StartPositionY,0), this.transform.rotation);
