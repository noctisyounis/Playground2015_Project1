    void Generate(int[] Room, int StartpositionX, int StartpositionY)
    {
        for (int i = 0; i < 30; i++)
        {
            for (int j = 0; j < 40; j++)
            {
                if (Room[i * 40 + j] == 1)
                {
                    GameObject g = (GameObject)GameObject.Instantiate(m_floor, new Vector3(j + StartpositionX + 0.5f, (i * -1) + StartpositionY - 0.5f, 0), this.transform.rotation);
                    g.transform.parent = this.transform;
                }

                if (Room[i * 40 + j] == 2)
                {
                    GameObject g = (GameObject)GameObject.Instantiate(m_roof, new Vector3(j + StartpositionX + 0.5f, (i * -1) + StartpositionY - 0.5f, 0), this.transform.rotation);
                    g.transform.parent = this.transform;
                }

                if (Room[i * 40 + j] == 3)
                {
                    GameObject g = (GameObject)GameObject.Instantiate(m_wall, new Vector3(j + StartpositionX + 0.5f, (i * -1) + StartpositionY - 0.5f, 0), this.transform.rotation);
                    g.transform.parent = this.transform;
                }

                if (Room[i * 40 + j] == 4)
                {
                    GameObject g = (GameObject)GameObject.Instantiate(m_triggerVertical, new Vector3(j + StartpositionX + 0.5f, (i * -1) + StartpositionY - 0.5f, 0), this.transform.rotation);
                    ChangeRoomTrigger f = g.GetComponent<ChangeRoomTrigger>();

                    //Rechercher les transform correspondant a chaque salle dans le tableau.
                    f.m_firstRoom = Dungeon[StartpositionX / 40, (StartpositionY / 30) * -1];
                    f.m_secondRoom = Dungeon[(StartpositionX / 40) + 1, (StartpositionY / 30) * -1];

                    //Mettre un objet en tant qu'enfant d'un objet.
                    g.transform.parent = this.transform;
                }

                if (Room[i * 40 + j] == 5)
                {
                    GameObject g = (GameObject)GameObject.Instantiate(m_triggerHorizontal, new Vector3(j + StartpositionX + 0.5f, (i * -1) + StartpositionY - 0.5f, 0), this.transform.rotation);
                    ChangeRoomTrigger f = g.GetComponent<ChangeRoomTrigger>();

                    f.m_firstRoom = Dungeon[StartpositionX / 40, (StartpositionY /30) * -1];
                    f.m_secondRoom = Dungeon[StartpositionX / 40, ((StartpositionY / 30) * -1) + 1];
                    
                    g.transform.parent = this.transform;
                }
            }
        }
    }

